    private boolean validarForm(){
        
        boolean result= false;
        
        if(this.txtNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL NOMBRE DEL ESTADIO", "NOMBRE", ERROR_MESSAGE);
        }else{
            result=true;
        }
        return result;
        
    }
    
    private void limpiar(){
        this.txtNombre.setText("");
    }
    
    private void cargarTabla(){
        
        DefaultTableModel table = new DefaultTableModel(Configuracion.estadios.getMatrizEstadios(),
                Configuracion.estadios.titulosEstadios());
        
        this.tablaEstadios.setModel(table);
        
    }
    
