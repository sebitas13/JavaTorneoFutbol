    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        try {
            
            int fila = this.tablaEstadios.getSelectedRow();
        int columna = 0;
        
        String estadio = (String)this.tablaEstadios.getValueAt(fila, columna);
        Estadio e = Configuracion.estadios.buscarEstadioPorNombre(estadio);
        if(e.getEquipo()!=null){
           e.getEquipo().setEstadio(null);  
        }
       
        
        
        
        
        int confirmado = JOptionPane.showConfirmDialog(this,"Â¿seguro?");
                
                if(JOptionPane.OK_OPTION==confirmado){
                    
                 Configuracion.estadios.getEstadios().remove(e);
                 JOptionPane.showMessageDialog(this, "ELIMINADO");
                this.cargarTabla();
                }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e);
        }
        
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

