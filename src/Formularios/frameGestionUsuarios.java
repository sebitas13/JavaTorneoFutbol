            int fila=0;
            int columna =1;
            String nombre="";
            //Jugador jugador;
            //Arbitro arbitro;
            Usuario usuario;


                fila=this.tblUsuarios.getSelectedRow();

                // JOptionPane.showMessageDialog(this, "Fila escogida: "+fila);

                

                //  JOptionPane.showMessageDialog(this, "Posicion escogida: "+posicion);

                usuario=Configuracion.usuarios.getUsuarios().get(fila);
                        

                if(usuario != null){

                    int confirmado = JOptionPane.showConfirmDialog(this,"Â¿seguro?");

                    if(JOptionPane.OK_OPTION==confirmado){

                        Configuracion.usuarios.getUsuarios().remove(usuario);
                        
                        this.cargarTabla();

                        JOptionPane.showMessageDialog(this, "Eliminado");

                    }

                }else{
                    JOptionPane.showMessageDialog(this,"User no encontrado");
                }

            

            this.cargarTabla();

