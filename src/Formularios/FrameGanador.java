    private void ganador(){
   
        try {
            
              JOptionPane.showMessageDialog(this,"GENERANDO GANADOR..: "+
                Configuracion.equipos.getEquipos().size()+" CLUBES  ");
     
        
        ArrayList<Equipo> equipos = Configuracion.equipos.getEquipos();
         ArrayList<Equipo> equiposCopia = new ArrayList<Equipo>(equipos);
        Collections.sort(equiposCopia,Collections.reverseOrder());
        
        mayor = equiposCopia.get(0);
        menor = equiposCopia.get(1);
        
        matriz[0] = mayor.getNombre();
        matriz[1] = menor.getNombre();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: "+e);
        }
        
      
        
    }
    
