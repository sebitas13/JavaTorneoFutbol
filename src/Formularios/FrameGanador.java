     private void generarGanador(){
         ganador=false;
         boolean g=false;
         int golesLocal =(int) this.spLocal1.getValue() + (int) this.spLocal2.getValue();
         int golesVisitante = (int) this.spVuelta1.getValue() + (int) this.spVuelta2.getValue() ;
         
         if(golesLocal>golesVisitante){
             this.equipo= mayor;
             ganador=true;
         }else if(golesLocal<golesVisitante){
             this.equipo=menor;
             ganador=true;
         }else{
             JOptionPane.showMessageDialog(this, "3er PARTIDO");
             g=true;
         }
         
         if(g){
             
             this.panIda1.setVisible(false);
             this.panVuelta.setVisible(false);
             this.panEmpate.setVisible(true);
             this.combo.setEnabled(false);
             ganador=false;
         }
         
     }
     
     private void generarGanadorExtra(){
         
         
         boolean g=false;
         int golesLocal =(int) this.spLocal3.getValue();
         int golesVisitante = (int) this.spVuelta3.getValue();
         
         if(golesLocal>golesVisitante){
             this.equipo= mayor;
             ganador=true;
         }else if(golesLocal<golesVisitante){
             this.equipo=menor;
             ganador=true;
         }else{
             g=true;
             JOptionPane.showMessageDialog(this, "PENALES");
         }
         
         if(g){
             this.panIda1.setEnabled(false);
             this.panVuelta.setEnabled(false);
             this.panEmpate.setEnabled(true);
             ganador=false;
         }
         
         
     }
     
     
