    private void tblResultadosEncuentrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultadosEncuentrosMouseClicked
        // TODO add your handling code here:
        
        try {
            int columna=6;
            int fila=0;
            String id="";
             fila = this.tblResultadosEncuentros.getSelectedRow();
             
             id = (String)this.tblResultadosEncuentros.getValueAt(fila, 6);
             
             Encuentro e = f.obtenerEncuentro(id);
             
             //JOptionPane.showMessageDialog(this, e.mostrarResultado());
             JTextArea textArea = new JTextArea(e.mostrarResultado());
            JScrollPane scrollPane = new JScrollPane(textArea);  
            textArea.setLineWrap(true);  
            textArea.setWrapStyleWord(true); 
            scrollPane.setPreferredSize( new Dimension( 500, 500 ) );
            JOptionPane.showMessageDialog(null, scrollPane, "ESTADISTICAS",  
                                       JOptionPane.YES_NO_OPTION);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR:"+e);
        }
        
    }//GEN-LAST:event_tblResultadosEncuentrosMouseClicked

