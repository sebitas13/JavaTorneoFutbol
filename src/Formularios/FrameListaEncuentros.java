    private void cargarcomboBox(){
        
         for(int i=0;i<Configuracion.equipos.cantidadDeEquipos()-1;i++){
            
            this.cbJornadas.addItem("Jornada "+(i+1));
            
        }
         
        for(int j=Configuracion.equipos.cantidadDeEquipos()-1;j<2*(Configuracion.equipos.cantidadDeEquipos()-1);j++){
            
            this.cbJornadas2.addItem("Jornada "+(j+1));
        } 
    }
    
    private void cargarTabla(int i){
        
        
        f= Configuracion.fechas.obtenerFechaPorJornada(i+1);
        
        if(f!=null){
           DefaultTableModel tabla = new DefaultTableModel(f.getMatrizResultadoEncuentros(),f.titulosEncuentros());
        this.tblResultadosEncuentros.setModel(tabla);
         
        this.tblResultadosEncuentros.setRowHeight(30);
        }
       
    }
    
