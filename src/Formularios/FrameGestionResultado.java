    private void crearEncuentro(Equipo e1,Equipo e2){
        
        //Creando encuentro
        
        encuentro1 = new Encuentro(e1.obtenerPlantel(jornada), e2.obtenerPlantel(jornada), e1.getEstadio()
                , this.jdcCalendario.getDate(), jornada,(Arbitro)this.cbArbitro.getSelectedItem());
        
        //Generar el codigo en el frame, una vez creado el encuentro,
        
        this.lblCodigoGenerado.setText(encuentro1.getId());
        
      
        
      
        
        
        //Agregando encuentro
      if( Configuracion.fechas.obtenerFechaPorJornada(jornada).agregarEncuentros(encuentro1)){
          JOptionPane.showMessageDialog(null, "Encuentro agregado");
      }else{
          JOptionPane.showMessageDialog(null, "Encuentro no agregado");
      }
        
        
    }
    
