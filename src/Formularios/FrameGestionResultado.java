   private void mostrarTiTuloJornada(){
       
       String texto="";
        if(jornada<Configuracion.equipos.cantidadDeEquipos()){
            texto = "Apertura";
        }else{
            texto = "Clausura";
        }
        
        if(texto.equals("Apertura")){
            this.lblJornadaDeJornada.setText(texto+"-"+"Jornada "+jornada
                +" de "+((Configuracion.equipos.cantidadDeEquipos()-1)));
        }else{
             this.lblJornadaDeJornada.setText(texto+"-"+"Jornada "+(jornada-(Configuracion.equipos.cantidadDeEquipos()-1))
                +" de "+((Configuracion.equipos.cantidadDeEquipos()-1)));
        }
        
   }
   
   
   private boolean asignarMinuto(String minuto){
       boolean result=false;
       
       if(!findMinuto(minuto)){
           this.minutos.add(minuto);
           result=true;
       }
       
       return result;
   }
   
   private boolean findMinuto(String minuto){
       boolean result=false;
       
       for(String m : this.minutos){
           if(minuto.equals(m)){
               result=true;break;
           }
       }
       
       
       return result;
   }
   
