    private void cargarComboBox(){
            
            Plantel p = this.equipo1.obtenerPlantel(jornada);
            Plantel p2 = this.equipo2.obtenerPlantel(jornada);
       if(p!=null && p2!=null){
           
          for(Convocado c : p.getConvocados()){
            
            this.cbConvocados1.addItem(c.getJugador().getNombre());
        }
        
         for(Convocado c : p2.getConvocados()){
            
            this.cbConvocados2.addItem(c.getJugador().getNombre());
        }          
           
       }else{
           JOptionPane.showMessageDialog(this, "Falta Convocar");
           
       }         
        
       
       //DEFINICION
       
       DefaultComboBoxModel comboD = new DefaultComboBoxModel(TipoGoles.values()); 
         this.cbTipogGolLocal.setModel(comboD);
        DefaultComboBoxModel comboE = new DefaultComboBoxModel(TipoGoles.values()); 
         this.cbTipoGolVisitante.setModel(comboE);
         
         //ARBITROS
         
        ArrayList<Arbitro> arbitros = Configuracion.personas.getListaArbitro();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for(Arbitro  a: arbitros){
            model.addElement(a);
        }
        this.cbArbitro.setModel(model);
        
        
        //TARJETAS
        
        DefaultComboBoxModel combo1 = new DefaultComboBoxModel(TipoTarjeta.values());
        this.cbTarjetas1.setModel(combo1);
        
        DefaultComboBoxModel combo2 = new DefaultComboBoxModel(TipoTarjeta.values());
        this.cbTarjetas2.setModel(combo2);
       
    }
    
