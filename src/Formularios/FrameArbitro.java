    private void cargarCombo(){
       
        //LLENADO COMBO CON ENUMERADOS
        DefaultComboBoxModel combo = new DefaultComboBoxModel(TipoArbitro.values()); 
         this.cboTipoArbitro.setModel(combo); 
        
        //LLENADO COMBO CON CADENAS
        
        for(int i=0;i<EnumPaises.values().length;i++){
             
             this.cboPaises.addItem(EnumPaises.values()[i].name());
         }
         
        
        
    }
    
    private boolean validarFormAgregar(){
        
        boolean result = false;
        if(this.txtNombre.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL NOMBRE DEL JUGADOR", "NOMBRE", ERROR_MESSAGE);
        
        }else if(Configuracion.personas.buscarJugadorPorNombre(this.txtNombre.getText())!=null){
            JOptionPane.showMessageDialog(this, "EL ARBITRO YA EXISTE", "NOMBRE",ERROR_MESSAGE);
            
        }else if(this.txtEdad.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA SU EDAD", "EDAD", ERROR_MESSAGE);
        }else if(this.txtSueldo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL SUELDO", "SUELDO", ERROR_MESSAGE);
        }else if(this.txtDNI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUSCA DNI", "DNI", ERROR_MESSAGE);
        }
        else if(Configuracion.personas.buscarJugadorPorDni(Integer.parseInt(txtDNI.getText()))!=null){
            JOptionPane.showMessageDialog(this, "DNI YA REGISTRADO ", "DNI", ERROR_MESSAGE);
        }
        
        else{
            result = true;
        }
        return result;     
        
    }
    
