    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
          try{
        int i = this.tablaEquipos.getSelectedRow();
        Equipo e = Configuracion.equipos.getEquipos().get(i);
        if(e != null){
            Configuracion.equipos.eliminar(e);
        }else{
            JOptionPane.showMessageDialog(this,"Equipo no encontrado");
        }
       
        DefaultTableModel tabla;
        tabla = new DefaultTableModel(Configuracion.equipos.getMatrizEquipos(),Configuracion.equipos.getTitulos());
        this.tablaEquipos.setModel(tabla);   
        
        }catch(IndexOutOfBoundsException p){
            JOptionPane.showMessageDialog(this, "FILA NO SELECCIONADA");
        }        
    }//GEN-LAST:event_btnEliminarActionPerformed

