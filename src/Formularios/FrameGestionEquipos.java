    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fila=0;
        int columna=0;
        String posicion="";
        Equipo equipo;
        
      try{
          
       fila=this.tablaEquipos.getSelectedRow();
        
       posicion= (String)this.tablaEquipos.getValueAt(fila,columna);
       
       equipo=Configuracion.equipos.getEquipos().get(Integer.parseInt(posicion)-1);
       
       FrameEquipo frm = new FrameEquipo(equipo);
       frm.setLocationRelativeTo(null);
       frm.setVisible(true);
       this.dispose();
       
          
      }catch(IndexOutOfBoundsException e){
        JOptionPane.showMessageDialog(this, "ERROR EN ELEGIR LA FILA: "+posicion);
      }  
       
       
    }//GEN-LAST:event_btnModificarActionPerformed

