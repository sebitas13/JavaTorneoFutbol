     private boolean validarModificacion(){
         boolean result=true;
         
          if(!this.txtNombre.getText().equals(this.j.getNombre())){
            if(Configuracion.personas.buscarJugadorPorNombre(this.txtNombre.getText())!=null){
              JOptionPane.showMessageDialog(this, "EL NOMBRE YA EXISTE", "NOMBRE",ERROR_MESSAGE);
              result=false;  
            }
            
          }else if(!this.txtDNI.getText().equals(String.valueOf(j.getDni()))){
              if(Configuracion.personas.buscarJugadorPorDni(Integer.parseInt(this.txtDNI.getText()))!=null){
                 JOptionPane.showMessageDialog(this, "DNI YA REGISTRADO", "DNI", ERROR_MESSAGE);
                 result=false; 
              }
              
               
          }
         
         return result;
     }
 
 
     
     private void cargarComboBox(){
         
         for(int i=0;i<EnumPaises.values().length;i++){
             
             this.cboPaises.addItem(EnumPaises.values()[i].name());
         }
         
         for(int i=0;i<Demarcacion.values().length;i++){
             
             this.cboPosiciones.addItem(Demarcacion.values()[i].name());
         }
         
         
     }
     
     
     
