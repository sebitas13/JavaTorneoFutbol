    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:

        
        Jugador jugador;

        try{

            if(!this.validarFormAgregar()){

            }else{
                
                Ubigeo ubigeo = (Ubigeo)this.cboDistritos.getSelectedItem();
              
                jugador = new Jugador(Integer.parseInt(this.txtNumero.getText()),
                        this.txtPais.getText(),this.txtNombre.getText(),
                        Integer.parseInt(this.txtEdad.getText()),Integer.parseInt(this.txtDNI.getText())
                        ,this.txtPosicion.getText(), Double.parseDouble(this.txtAltura.getText()),ubigeo);
                
               
                
                int confirmado = JOptionPane.showConfirmDialog(this,"¿seguro?");
                
                if(JOptionPane.OK_OPTION==confirmado){
                    
                Configuracion.personas.agregar(jugador);
                JOptionPane.showMessageDialog(this, "AGREGADO EXITOSAMENTE");
                this.limpiar();
                    
                    
                }
                
           
            }

        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "ERROR");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try{
            int p =0;
            if(!this.validarFormModificar()){

            }else{
                Ubigeo ubigeo = (Ubigeo)this.cboDistritos.getSelectedItem();
                p = Configuracion.personas.posicionDeJugador(j);
                j.setNombre(this.txtNombre.getText());
                j.setPais(this.txtPais.getText());
                j.setNumero(Integer.parseInt(this.txtNumero.getText()));
                j.setEdad(Integer.parseInt(this.txtEdad.getText()));
                j.setDni(Integer.parseInt(this.txtDNI.getText()));
                j.setPosicionJ(this.txtPosicion.getText());
                j.setAltura(Double.parseDouble(this.txtAltura.getText()));
                j.setUbigeo(ubigeo);
                Configuracion.personas.getPersonas().set(p, j);
                JOptionPane.showMessageDialog(this, "MODIFICADO EXITOSO");

                FrameGestionPersonas frm = new FrameGestionPersonas();
                frm.setLocationRelativeTo(null);
                frm.setVisible(true);
                this.dispose();
            }

        }catch(IndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"ERROR: "+e);
        }

    }//GEN-LAST:event_btnModificarActionPerformed

