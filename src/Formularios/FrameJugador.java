private boolean validarFormAgregar(){
        boolean result = false;
        if(this.txtNombre.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL NOMBRE DEL JUGADOR", "NOMBRE", ERROR_MESSAGE);
        
        }else if(Configuracion.personas.buscarJugadorPorNombre(this.txtNombre.getText())!=null){
            JOptionPane.showMessageDialog(this, "EL JUGADOR YA EXISTE", "NOMBRE",ERROR_MESSAGE);
            
        }else if(this.txtAltura.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA LA ALTURA", "ALTURA", ERROR_MESSAGE);
        }else if(this.txtDNI.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL DNI", "DNI", ERROR_MESSAGE);
        }else if(Configuracion.personas.buscarJugadorPorDni(Integer.parseInt(txtDNI.getText()))!=null){
            JOptionPane.showMessageDialog(this, "DNI YA REGISTRADO ", "DNI", ERROR_MESSAGE);
        }
        else if(this.txtEdad.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA LA EDAD", "EDAD", ERROR_MESSAGE);
        }else if(this.txtNumero.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL NUMERO", "NUMERO", ERROR_MESSAGE);
        }else if(this.txtPais.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA EL PAIS", "PAIS", ERROR_MESSAGE);
        }else if(this.txtPosicion.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INTRODUZCA POSICION", "POSICION", ERROR_MESSAGE);
        }else if(this.cboDistritos.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this, "ELIGA DISTRITO", "DISTRITO", ERROR_MESSAGE);
        }
        
        else{
            result = true;
        }
        return result;     
    } 




